package ch02_basicprogramming.unit04_datatype;

/* 
    - 확장열 (Escape sequence)
        - 문자열 내에서 특수한 의미를 가지는 문자 조합
*/

public class Ex07_EscapeSequence {
    public static void main(String[] args) {        
        System.out.println("a\tb\tc");
        System.out.println("a\nb\nb");
        System.out.println("x\\y"); // \출력
        System.out.println("\101"); // 8진수, 65 -> A, '\101'가능(문자 한 글자 이므로)
        System.out.println("\u0043"); // 유니코드, 67 -> C, '\u0043'가능(문자 한 글자 이므로)
    }
}


/* CONCEPT -------------------------------------------------------------------------------------------------------------

    1. 이스케이프 시퀀스 (Escape Sequence)
        - 문자열 내에서 특수 문자나 제어 문자를 표현하기 위한 방식
        - 백슬래시 '\'를 앞에 붙여 사용함
        - 일부는 C 언어에서 유래했으며, Java는 그 중 일부만 지원함
    
        | 시퀀스   | 의미                                  |
        |---------|-------------------------------------|
        | \n      | 줄 바꿈 (Line Feed)                   |
        | \r      | 캐리지 리턴 (Carriage Return)          |
        | \t      | 수평 탭 (Tab)                         | 
        | \b      | 백스페이스                             |
        | \f      | 폼 피드 (페이지 넘김 등, 거의 사용 안 함)    |
        | \'      | 작은 따옴표 (문자 리터럴 내부에서 사용)      |
        | \"      | 큰 따옴표 (문자열 내부에서 사용)            |
        | \\      | 역슬래시 `\` 자체 출력                   |
    
    ** Java에서는 아래는 사용 불가
        | 시퀀스    | 의미                                 |
        |---------|-------------------------------------|
        | \a      | (Alert, Bell) X                     |
        | \0      | (Null 문자) X                        |
    
        -> 이들은 C/C++에서 사용되는 시퀀스
    
    2. 8진수, 유니코드 리터럴
        - \ddd: 8진수 ASCII 값 -> 문자 
            - \101 -> 65 (10진수) -> 'A'
        - \u0000: 유니코드 (16진수 4자리)
            - 예) \u0043 -> 67 (10진수) -> 'C'
    
    3. 주의사항
        - \101, \u0043처럼 '문자 하나'로 간주되는 경우 char 타입에도 대입 가능
        - 문자열 출력에서도 그대로 쓰이면 해당 문자로 해석됨

--------------------------------------------------------------------------------------------------------------------- */
