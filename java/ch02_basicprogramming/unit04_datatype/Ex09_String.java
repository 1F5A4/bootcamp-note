package ch02_basicprogramming.unit04_datatype;

/*
    - String  
        : 문자열을 나타내는 '클래스' = 참조형  
        : 참조형이지만 리터럴로 바로 사용 가능 ("hello")
*/

public class Ex09_String {
	public static void main(String[] args) {
		String s1, s2, s3;
		s1 = "seoul";
		s2 = "korea";
		s3 = s1 + " " + s2; // 문자열 + 문자열 => 문자열(문자열결합)
		System.out.println(s3); // seoul korea
		
		
		s3 = ""; // 길이가 0인 문자열
		System.out.println(s3);
		
		s3 = "X" + 'A' + 10;
		// 왼쪽부터 결합되며, 모든 항이 문자열로 변환됨
		// 문자열 + 일반리터널 => 문자열로 결합
		System.out.println(s3); // "XA10"
		
		s3 = 'A' + 10 + "X";
		// 먼저 정수 연산 수행 후 문자열 결합
			// 1) 문자 + 정수 => 정수: 65 + 10
			// 2) 정수 + 문자열 => 문자열로 결합
		System.out.println(s3); // "75X"
	}
}


/* CONCEPT -------------------------------------------------------------------------------------------------------------

    1. String 클래스: 문자열을 나타내는 참조형 타입
        - 문자열을 다루기 위한 자바의 표준 클래스 (java.lang.String)
        - 참조형(reference type)
        - 참조형이지만 'new' 없이도 리터럴로 사용 가능 (String s = "hello";)
    
    2. 문자열 결합
        - + 연산자를 사용해 문자열끼리 결합 가능
        - 문자열 + 어떤 타입 -> 문자열로 자동 변환
    
    3. 빈 문자열
        - "": 길이가 0인 문자열 (null 아님)
        - 출력하면 아무것도 나타나지 않음
    
    4. 주의할 점 *****
        - null과 ""는 다르다!
            - null: 참조 없음
            - "": 내용은 없지만 유효한 문자열 객체

--------------------------------------------------------------------------------------------------------------------- */
